	winrt::Windows::Foundation::Collections::IVector<winrt::Windows::Foundation::IInspectable> SvgPage::Pngs()
	{
		if (m_pngs)
			return m_pngs;

		auto svgs = Svgs();
		std::vector<winrt::Windows::Foundation::IInspectable> transformed;
		transformed.reserve(svgs.Size());
		for (auto const& item : svgs)
		{
			auto uri = item.as<winrt::Windows::Foundation::Uri>();
			std::wstring s{ uri.RawUri() };
			if (auto p = s.find(L"/svg/"); p != std::wstring::npos)
				s.replace(p, 5, L"/png/");
			if (auto e = s.rfind(L".svg"); e != std::wstring::npos)
				s.replace(e, 4, L".png");
			transformed.push_back(winrt::Windows::Foundation::Uri{ winrt::hstring{ s } });
		}
		m_pngs = winrt::single_threaded_vector(std::move(transformed));
		return m_pngs;
	}

	void SvgPage::ListViewScrollViewer_ViewChanged(winrt::Windows::Foundation::IInspectable const& sender, winrt::Microsoft::UI::Xaml::Controls::ScrollViewerViewChangedEventArgs const&)
	{
		auto source = sender.try_as<winrt::Microsoft::UI::Xaml::Controls::ScrollViewer>();
		if (!source)
			return;

		auto tag = winrt::unbox_value_or<winrt::hstring>(source.Tag(), L"");
		if (tag == L"Svg")
		{
			m_svgScroll = source;
			if (m_pngScroll)
				SyncFrom(source, m_pngScroll);
		}
		else if (tag == L"Png")
		{
			m_pngScroll = source;
			if (m_svgScroll)
				SyncFrom(source, m_svgScroll);
		}
	}

	void SvgPage::SyncFrom(winrt::Microsoft::UI::Xaml::Controls::ScrollViewer const& source, winrt::Microsoft::UI::Xaml::Controls::ScrollViewer const& target)
	{
		if (!source || !target || m_isSyncing)
			return;
		m_isSyncing = true;
		target.ChangeView(
			winrt::box_value(source.HorizontalOffset()).as<winrt::Windows::Foundation::IReference<double>>(),
			winrt::box_value(source.VerticalOffset()).as<winrt::Windows::Foundation::IReference<double>>(),
			nullptr,
			true);
		m_isSyncing = false;
	}
